DROP TABLE IF EXISTS ETU_ELEMENT;
DROP TABLE IF EXISTS ETU_VALIDATION;
DROP TABLE IF EXISTS ETU_RESULTAT;
DROP TABLE IF EXISTS ETU_RECONNAISSANCE;

CREATE TABLE ETU_ELEMENT (
       ID INT PRIMARY KEY,
       PARENT INT,
       TITLE VARCHAR(50)
);

CREATE TABLE ETU_VALIDATION (
       ID INT PRIMARY KEY,
       ETU_ELEMENT_ID INT,
       GRADE REAL,
       ECTS REAL,
       ETAT_VALIDATION VARCHAR(50)
);

CREATE TABLE ETU_RESULTAT (
       ID INT PRIMARY KEY,
       ETU_ELEMENT_ID INT,
       TRIES INT,
       GRADE REAL
);

CREATE TABLE ETU_RECONNAISSANCE (
       ID INT PRIMARY KEY,
       ETU_ELEMENT_ID INT,
       ETU_ELEMENT_ID_RECONNU INT,
       TYPE VARCHAR(50),
       GRADE REAL,
       ECTS REAL
);

